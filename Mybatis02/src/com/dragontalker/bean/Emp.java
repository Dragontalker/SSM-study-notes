package com.dragontalker.bean;

public class Emp {
	
	private Integer eid;
	
	private String ename;
	
	private Integer age;
	
	private String sex;
	
	public Emp() {}

	public Emp(Integer eid, String ename, Integer age, String sex) {
		this.eid = eid;
		this.ename = ename;
		this.age = age;
		this.sex = sex;
	}

}
